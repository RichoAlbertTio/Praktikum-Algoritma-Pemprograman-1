#include<iostream>
using namespace std;
const int bsoni=15;

int main()
{
	int badi,banis,bluki,bnisa;
	badi=bsoni-10;
	banis=(2*bsoni)+badi;
	bluki=bsoni+badi+banis-5;
	bnisa=bluki*4;
	cout<<"buku badi "<<badi<<endl;
	cout<<"buku banis "<<banis<<endl;
	cout<<"buku bluki "<<bluki<<endl;
	cout<<"buku bnisa "<<bnisa<<endl;
	return 0;
}

